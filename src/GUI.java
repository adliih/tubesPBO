    public void loginMember(String username, String password) throws SQLException{
        int i = tf.searchMember(username);
        if (i >= 0){
            Member m = tf.getMember(i);
            if(m.getPassword().equals(db.encryptPassword(password))){
                OrangAktif = m;
            }
        }
    }
